const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};


function decode(expr) {
    expr = expr.split('');
    let symb = '';
    let string = '';

    for (let i = 0; i < expr.length - 1; i = i + 2){
        if (expr[i] == '*'){
            string += ' ';
            i += 10;
        }
        if (expr[i] === '1'){
            expr[i+1] === '0' ? (symb += '.'): (symb += '-');
        }
        if ((i+2) % 10 === 0){
            string += MORSE_TABLE[symb];
            symb = '';
        }
    }
    return string;
}

module.exports = {
    decode
}